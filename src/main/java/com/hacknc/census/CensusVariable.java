    private static Map<String, CensusVariable> reverse;

    static {
        reverse = new HashMap<>();
        for (CensusVariable v : CensusVariable.values()) {
            reverse.put(v.get(), v);
        }
    }

    private String name;
    private boolean isNormalizable;

    CensusVariable(String name, boolean normalizable) {
        this.name = name;
        isNormalizable = normalizable;
    }

    CensusVariable(String name) {
        this(name, false);
    }

    public static Set<String> variableNames() {
        return reverse.keySet();
    }

    public static CensusVariable reverseLookup(String variableName) {
        return reverse.get(variableName);
    }

    public String get() {
        return name;
    }

    public boolean isNormalizable() {
        return isNormalizable;
    }
